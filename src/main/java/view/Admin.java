    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (customerPanel.isVisible() == true) {
            int id;
            id = Integer.parseInt(cstModel.getValueAt(row, 0).toString());
            Customer cst = new Customer(id);
            CustomerController cstController = new CustomerController();
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete selected record?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == 0) {
                cstController.deleteCustomer(cst);
                JOptionPane.showMessageDialog(rootPane, "Data has been deleted successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                this.readCustomer();
            }
        } else if (categoryPanel.isVisible() == true) {
            int id;
            id = Integer.parseInt(catModel.getValueAt(row, 0).toString());
            ProductCategory pCat = new ProductCategory(id);
            ProductCategoryController catController = new ProductCategoryController();
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete selected record?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == 0) {
                catController.deleteProductCategory(pCat);
                JOptionPane.showMessageDialog(rootPane, "Data has been deleted successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                this.readCategory();
            }
        } else if (productPanel.isVisible() == true) {
            String sku;
            sku = pvModel.getValueAt(row, 0).toString();
            ProductVariants productVariants = new ProductVariants(sku);
            ProductVariantsController pvController = new ProductVariantsController();
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete selected record?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == 0) {
                pvController.deleteProductVariants(productVariants);
                JOptionPane.showMessageDialog(rootPane, "Data has been deleted successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                this.readProductVariants();
                this.readOrderHistory();
            }
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

