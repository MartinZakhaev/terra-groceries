    public void readOrderHistory() {
        ohModel = new DefaultTableModel();
        ohModel.addColumn("Transaction ID");        
        ohModel.addColumn("Customer ID");
        ohModel.addColumn("Product SKU");
        ohModel.addColumn("Qty");        
        ohModel.addColumn("Transaction Time");
        jTable4.setModel(ohModel);
        ConnectionManager conman = new ConnectionManager();
        Connection con = conman.getConnection();
        try {
            Statement stm = con.createStatement();
            String query = "SELECT * FROM transaction_history";
            ResultSet res = stm.executeQuery(query);
            while (res.next()) {
                ohModel.addRow(new Object[] {
                    res.getString("id"),
                    res.getString("customer_id"),
                    res.getString("sku"),
                    res.getString("total_item"),
                    res.getString("transaction_time"),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
