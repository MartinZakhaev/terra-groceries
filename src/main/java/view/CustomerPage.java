    public void readData(int id) {
        ResultSet res = null;
        setCstId(id);
        orderHistory = new DefaultTableModel();
        orderHistory.addColumn("Product SKU");
        orderHistory.addColumn("Price");
        orderHistory.addColumn("Qty");
        orderHistory.addColumn("Total Transaction");
        orderHistory.addColumn("Transaction Time");
        jTable1.setModel(orderHistory);
        Customer cst = new Customer(id);        
        CustomerController cstController = new CustomerController();
        res = cstController.ohCustomer(cst);
        try {
            while (res.next()) {
                orderHistory.addRow(new Object[] {
                    res.getString("sku"),
                    res.getString("price"),
                    res.getString("total_item"),
                    res.getString("total_price"),
                    res.getString("transaction_time"),                    
                });                                
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void readProductVariants() {
        ResultSet res = null;
        productVariants = new DefaultTableModel();
        productVariants.addColumn("SKU");
        productVariants.addColumn("Product Name");
        productVariants.addColumn("Size");
        jTable2.setModel(productVariants);
        ProductVariantsController pvController = new ProductVariantsController();
        res = pvController.pvCustomer();
        try {
            while (res.next()) {
                productVariants.addRow(new Object[] {
                    res.getString("sku"),
                    res.getString("name"),
                    res.getString("size"),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
