    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id, total_item;
        String sku;
        id = getCstId();
        sku = jTextField7.getText();
        total_item = Integer.parseInt(jTextField11.getText());
        Customer cst = new Customer(id);
        CustomerController cstController = new CustomerController();
        int confirm = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to continue?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            cstController.thCustomer(cst, sku, total_item);
            jTextField7.setText("");
            jTextField8.setText("");
            jTextField9.setText("");
            jTextField10.setText("");
            jTextField11.setText("");
            jTextField12.setText("");
            JOptionPane.showMessageDialog(rootPane, "Thank you for your order. Your order has been successfully processed", "Information", JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(rootPane, "You can view your order history in the order history menu", "Information", JOptionPane.INFORMATION_MESSAGE);
            this.readData(id);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

